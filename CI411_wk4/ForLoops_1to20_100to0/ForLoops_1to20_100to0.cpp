#include <iostream>

int main()
{
    // To make a loop that counts from 1 to 20

    for (int i = 1; i <= 20; i++)
    {
		std::cout << i << std::endl;
	}

    // To make a loop that counts from 100 to 0 in intervals of 5

    for (int i = 100; i >= 0; i -= 5)
    {
        std::cout << i << std::endl;
    }

}